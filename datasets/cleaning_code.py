"""Scheduler Queue"""

from collections import deque
scheduler_queue = deque(task_queue)

task = scheduler_queue.popleft() #getting and storing next task record

def can_allocate(task, vm):

    cpu_ok = (
        vm["cpu_used"] + task["cpu_request"]
        <= vm["cpu_capacity"]
    )

    mem_ok = (
        vm["memory_used"] + task["memory_request"]
        <= vm["memory_capacity"]
    )

    return cpu_ok and mem_ok

def assign_task(task, vm):

    vm["cpu_used"] += task["cpu_request"]
    vm["memory_used"] += task["memory_request"]

    return vm

def cpu_utilization(vm):

    return (
        vm["cpu_used"]
        / vm["cpu_capacity"]
    )


def memory_utilization(vm):

    return (
        vm["memory_used"]
        / vm["memory_capacity"]
    )

