def extract_resources(x):
    try:
        if isinstance(x, dict):
            resource_dict = x
        else:
            resource_dict = ast.literal_eval(str(x))

        return pd.Series([
            resource_dict.get("cpus", 0),
            resource_dict.get("memory", 0)
        ])
    except:
        return pd.Series([0, 0])

df[["cpu_request", "memory_request"]] = (
    df["resource_request"].apply(extract_resources)
)

