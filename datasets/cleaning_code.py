IDLE_POWER = 100
MAX_POWER = 250
def power_consumption(vm):

    utilization = cpu_utilization(vm)

    power = (
        IDLE_POWER
        + utilization *
        (MAX_POWER - IDLE_POWER)
    )

    return power

